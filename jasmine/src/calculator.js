var Calculator = function() {

    this.add = function(num1, num2) {
        return num1 + num2;
    };

    this.sub = function(num1, num2) {
        return num1 - num2;
    };

    this.multi = function(num1, num2) {
        return num1 * num2;
    };

    this.div = function(num1, num2) {
        return num1 / num2;
    };
};